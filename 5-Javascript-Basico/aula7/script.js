/*
adição: +
subtração: -
multiplicação: *
divisão: /
incremento: ++
decremento: --
atribuição +=
*/


let a = 10
let b = 5
let c = a + b
console.log(c)


// incremento

let m = 10
m++
console.log(m)



// decremento

let n = 10
n--
console.log(n)

// atribuição
let o = 12
o += 25  // o = o + 25
console.log(o)


